"""Build and write Obsidian-visible markdown sidecars."""

from __future__ import annotations

import os
from pathlib import Path
from typing import List

from . import __version__


def build_markdown(source: Path, pages: List[str]) -> str:
    """Compose sidecar markdown for a source file given its OCR'd page texts.

    Images produce a single "page"; PDFs produce one per page.
    """
    generated_by = f"obsidian-ocr {__version__}"
    lines = [
        "---",
        f"source: {source.name}",
        f"generated_by: {generated_by}",
        f"pages: {len(pages)}",
        "---",
        "",
        f"> OCR transcript of `{source.name}` (generated by {generated_by}).",
        "",
        f"![[{source.name}]]",
        "",
    ]

    if len(pages) == 1:
        lines.append(pages[0].strip())
    else:
        for index, text in enumerate(pages, start=1):
            lines.append(f"## Page {index}")
            lines.append("")
            lines.append(text.strip())
            lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def write_sidecar(sidecar: Path, content: str) -> None:
    """Atomically write `content` to `sidecar`."""
    tmp = sidecar.with_name(sidecar.name + ".tmp")
    tmp.write_text(content, encoding="utf-8")
    os.replace(tmp, sidecar)
